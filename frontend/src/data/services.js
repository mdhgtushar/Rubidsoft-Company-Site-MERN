// Helper function to get service by ID
export const getServiceById = (id) => {
  return services.find(service => service.id === id);
};

// Helper function to get services by category
export const getServicesByCategory = (category) => {
  if (category === "all") return services;
  return services.filter(service => service.category === category);
};

// Helper function to get all categories
export const getAllServiceCategories = () => {
  const categories = services.map(service => service.category);
  return ["all", ...new Set(categories)];
};

// Helper function to get services by search term
export const searchServices = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return services.filter(service => 
    service.title.toLowerCase().includes(term) ||
    service.description.toLowerCase().includes(term) ||
    service.category.toLowerCase().includes(term)
  );
};

// Helper function to get featured services
export const getFeaturedServices = (count = 4) => {
  return services.slice(0, count);
};

// Helper function to get related services
export const getRelatedServices = (currentServiceId, count = 3) => {
  const currentService = getServiceById(currentServiceId);
  if (!currentService) return [];
  
  return services
    .filter(service => service.id !== currentServiceId && service.category === currentService.category)
    .slice(0, count);
}; 